public void decorateTableTask(){
    
    //Customizando o header da tabela ou table de tarefas
    jTableTask.getTableHeader().setFont(new Font("Segoe UI", Font.BOLD, 14));
    jTableTask.getTableHeader().setBackground(new Color(0, 204, 102));
    jTableTask.getTableHeader().setForeground(new Color(255, 255, 255));
    //pode ordenar as colunas por ordem alfabetica, por data...
    jTableTask.setAutoCreateRowSorter(true);
    jTableTask.getColumnModel().getColumn(2).setCellRenderer(new DeadlineColumnRed());
    jTableTask.getColumnModel().getColumn(4).setCellRenderer(new ButtonColumnCellRederer("edit"));
    jTableTask.getColumnModel().getColumn(5).setCellRenderer(new ButtonColumnCellRederer("delete"));
    

}

public void initDataControler(){
    
    projectControler = new ProjetoControler();
    tarefaControler = new TarefaControler();
}

public void initComponetsModel(){
    
    projectModel = new DefaultListModel();
    loadProjects();
    tarefaModel = new TarefaTableModel();
    jTableTask.setModel(tarefaModel);
   
// loadTasks(2);

    if(!projectModel.isEmpty()){
        jListProjects.setSelectedIndex(0);
        Projeto projeto = (Projeto) projectModel.get(0);
        loadTasks(projeto.getId());
    }
    
    
}

public void loadTasks(int idProjeto){
    List<Tarefa> tarefas = tarefaControler.getAll(idProjeto);
    tarefaModel.setTarefas(tarefas);
    showJTableTarefas(!tarefas.isEmpty());
}

private void showJTableTarefas(boolean hasTarefas){
    //se Existem Tarefas foram carregadas
    if (hasTarefas) {
        if (jPanelEmptList.isVisible()){
            jPanelEmptList.setVisible(false);
            jPanel5.remove(jPanelEmptList);
        }
        jPanel5.add(jScrollPane1);
        jScrollPane1.setVisible(true);
        jScrollPane1.setSize(jPanel5.getWidth(),jPanel5.getHeight());     
        
    }
    else {
        if (jScrollPane1.isVisible()){
            jScrollPane1.setVisible(false);
            jPanel5.remove(jScrollPane1);
        }
    } jPanel5.add(jPanelEmptList);
      jPanelEmptList.setVisible(true);
      jPanelEmptList.setSize(jPanel5.getWidth(), jPanel5.getHeight());
}

public void loadProjects(){
    
    List<Projeto> projetos = projectControler.getAll();
    
    projectModel.clear();
    
    for (int i = 0; i < projetos.size(); i++) {
        
        Projeto projeto = projetos.get(i);
        projectModel.addElement(projeto);       
    }
    jListProjects.setModel(projectModel);
    
}

