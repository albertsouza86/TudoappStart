    private void jTableTaskMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableTaskMouseClicked
        // TODO add your handling code here:
        int rowIndex = jTableTask.rowAtPoint(evt.getPoint());
        int columnIndex = jTableTask.columnAtPoint(evt.getPoint());
        Tarefa tarefa = tarefaModel.getTarefas().get(rowIndex);
        
        switch(columnIndex){
             
          case 3:
              
              tarefaControler.update(tarefa);
              break;
            
            case 4:
              break;
            
            case 5:
                
                tarefaControler.removebyid(tarefa.getId());
                tarefaModel.getTarefas().remove(tarefa);
                int projectIndex = jListProjects.getSelectedIndex();
                Projeto projeto = (Projeto) projectModel.get(projectIndex);
                loadTasks(projeto.getId());
                        
                        
              break;
              
        }
    }//GEN-LAST:event_jTableTaskMouseClicked

